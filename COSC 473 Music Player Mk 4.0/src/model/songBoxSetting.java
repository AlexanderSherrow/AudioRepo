package model;
import javafx.scene.media.Media;
public class songBoxSetting
{
	double height;
	double width;
	
	public songBoxSetting(double height, double width) 
	{
		super();
		this.height = height;
		this.width = width;

	}

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}

	public double getWidth() {
		return width;
	}

	public void setWidth(double width) {
		this.width = width;
	}



	
	
}
